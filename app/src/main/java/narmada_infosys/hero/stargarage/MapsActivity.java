    @Override
    public void onMapReady(GoogleMap googleMap) {

        mMap = googleMap;

        // Make sure that GPS is enabled on the device
        LocationManager mlocManager = (LocationManager) this.getSystemService(Context.LOCATION_SERVICE);
        boolean enabled = mlocManager.isProviderEnabled(LocationManager.GPS_PROVIDER);

        if (!enabled) {
            showDialogGPS();
        }


        // Add a marker in Star garage and move the camera
        LatLng star_garage = new LatLng(28.957488, 80.182951);
        mMap.addMarker(new MarkerOptions().position(star_garage).title("Star Garage")).showInfoWindow();
        mMap.moveCamera(CameraUpdateFactory.newLatLng(star_garage));

        final CameraPosition MARKER_POS = new CameraPosition.Builder()
                .target(star_garage)
                .zoom(15)
                .bearing(0)
                .tilt(25)
                .build();
        mMap.animateCamera(CameraUpdateFactory.newCameraPosition(MARKER_POS));

        /**
         * Show a dialog to the user requesting that GPS be enabled
         */

        getRouteToMarker(star_garage);

        //Test

        ///Test
    }

    private void getRouteToMarker(LatLng star_garage) {

        if (ActivityCompat.checkSelfPermission(this, Manifest.permission.ACCESS_FINE_LOCATION) != PackageManager.PERMISSION_GRANTED && ActivityCompat.checkSelfPermission(this, Manifest.permission.ACCESS_COARSE_LOCATION) != PackageManager.PERMISSION_GRANTED) {
            // TODO: Consider calling
            //    ActivityCompat#requestPermissions
            // here to request the missing permissions, and then overriding
            //   public void onRequestPermissionsResult(int requestCode, String[] permissions,
            //                                          int[] grantResults)
            // to handle the case where the user grants the permission. See the documentation
            // for ActivityCompat#requestPermissions for more details.
            return;
        }
        Location location = LocationServices.FusedLocationApi.getLastLocation(googleApiClient);
        if (location != null) {
            Routing routing = new Routing.Builder()
                    .travelMode(AbstractRouting.TravelMode.DRIVING)
                    .withListener(this)
                    .alternativeRoutes(false)
                    .waypoints(new LatLng(location.getLongitude(), location.getLatitude()), star_garage)
                    .build();
            routing.execute();

            moveMap();
        }

    }


